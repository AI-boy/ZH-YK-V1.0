void full_display(unsigned char data_left,unsigned char data_right)
{
	unsigned char i,j;
	
	for(i=0;i<8;i++)
	{
		LCD_CS=0;
		lcd_address(i+1,1);
		for(j=0;j<64;j++)
		{
			transfer_data(data_left);
			transfer_data(data_right);
		}
	}
}
