/**********************************************************************
*函数名称：init_lcd
*描述：指示灯初始化
*返回类型：void
*输入形参：void
***********************************************************************/
void init_lcd(void)
{
	//LCD 端口设置
	set_io_mode(PORT2,6,STAND);  //LCD 背光LED端口设为输出	P2.6
	
	LCD_LED_OFF;
	
	set_io_mode(PORT1,6,STAND);// CS  设置输出
	
	set_io_mode(PORT1,7,STAND);// RST  设置输出
	
	set_io_mode(PORT4,4,STAND);// RS  设置输出
	
	set_io_mode(PORT1,5,STAND);// SCK  设置输出
	
	set_io_mode(PORT1,3,STAND);// SDO  设置输出
	
	//LCD参数设置
	LCD_CS=1;
	LCD_RST=0; /*低电平复位*/
	delay_us(100);
	LCD_RST=1; /*复位完毕*/
	delay_us(100);
	
	transfer_command(0xe2); /*软复位*/
	delay_us(5);
	transfer_command(0x2c); /*升压步聚1*/
	delay_us(5);
	transfer_command(0x2e); /*升压步聚2*/
	delay_us(5);
	transfer_command(0x2f); /*升压步聚3*/
	delay_us(5);
	transfer_command(0x24); /*粗调对比度，可设置范围0x20～0x27*/
	transfer_command(0x81); /*微调对比度*/
	transfer_command(0x12); /*0x1a,微调对比度的值，可设置范围0x00～0x3f*/
	transfer_command(0xa2); /*1/9偏压比（bias）*/
	transfer_command(0xc8); /*行扫描顺序：从上到下*/
	transfer_command(0xa0); /*列扫描顺序：从左到右*/
	transfer_command(0x40); /*起始行：第一行开始*/
	transfer_command(0xaf); /*开显示*/
		
	//LCD_LED_ON;  //点亮背光
	
	clear_screen(); //clear all dots
}




//LCD写指令
void transfer_command(unsigned char dat)
{
	unsigned char i;
	
	LCD_CS=0;
	
	LCD_RS=0;
	
	for(i=0;i<8;i++)
	{
		LCD_SCK=0;
		
		delay_us(2);
		
		if(dat&0x80) 
		{
			LCD_SDO=1;
		}
		else 
		{
			LCD_SDO=0;
		}
		
		LCD_SCK=1;
		
		delay_us(2);
		
		dat=dat<<=1;
	}
	
	LCD_CS=1;
	
}


/*写数据到LCD模块*/
void transfer_data(unsigned char dat)
{
	unsigned char i;
	
	LCD_CS=0;
	
	LCD_RS=1;
	
	for(i=0;i<8;i++)
	{
		LCD_SCK=0;
		
		delay_us(2);
		
		if(dat&0x80) 
		{
			LCD_SDO=1;
		}
		else 
		{
			LCD_SDO=0;
		}
		
		LCD_SCK=1;
		
		delay_us(2);
		
		dat=dat<<=1;
	}
	
	LCD_CS=1;
	
}

//设置LCD显示起始地址（页，列）  12864：共8页 每页128列  128*8*8=128*64
void lcd_address(unsigned char page,unsigned char column)
{
	column=column-1; 				//我们平常所说的第1列，在LCD驱动IC里是第0列。所以在这里减去1

	page=page-1;
	
	transfer_command(0xb0+page); 	//设置页地址。每页是8行。一个画面的64行被分成8个页。我们平常所说的第1页，在LCD驱动IC里是第0页，所以在这里减去1
	
	transfer_command(((column>>4)&0x0f)+0x10); 	//设置列地址的高4位
	transfer_command(column&0x0f); 				//设置列地址的低4位
}


//清整个屏幕
void clear_screen(void)
{
	unsigned char i,j;
	
	for(i=0;i<9;i++)
	{
		lcd_address(1+i,1);
		
		for(j=0;j<132;j++)
		{
			transfer_data(0x00);
		}
	}
}


